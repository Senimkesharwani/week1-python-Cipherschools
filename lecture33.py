# String Methods Part 1 in Python
name="Senim Kesharwani"
#length count
print(len(name))

#lower case
print(name.lower())

#upper case
print(name.upper())

#title case
print(name.title())  # Title variable Make starting letter Capital

#count case
print(name.count("h"))

#replace case
print(name.replace("h","k"))


